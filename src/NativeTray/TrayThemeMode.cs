namespace NativeTray;

/// <summary>
/// Describes the theme mode to apply to the system menu
/// </summary>
public enum TrayThemeMode
{
    /// <summary>
    /// None predefined theme mode
    /// </summary>
    None,

    /// <summary>
    /// Dark predefined theme mode.
    /// </summary>
    Dark,

    /// <summary>
    /// Light predefined theme mode
    /// </summary>
    Light,

    /// <summary>
    /// System predefined theme mode
    /// </summary>
    System,
}
