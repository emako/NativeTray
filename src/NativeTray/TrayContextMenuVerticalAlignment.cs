namespace System.NativeTray;

/// <summary>
/// Specifies how the tray context menu is vertically aligned relative to the popup anchor point.
/// </summary>
public enum TrayContextMenuVerticalAlignment
{
    /// <summary>
    /// Aligns the top edge of the menu with the anchor point.
    /// </summary>
    Top,

    /// <summary>
    /// Aligns the vertical center of the menu with the anchor point.
    /// </summary>
    Center,

    /// <summary>
    /// Aligns the bottom edge of the menu with the anchor point.
    /// </summary>
    Bottom,
}