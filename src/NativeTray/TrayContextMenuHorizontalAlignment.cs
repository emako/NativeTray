namespace System.NativeTray;

/// <summary>
/// Specifies how the tray context menu is horizontally aligned relative to the popup anchor point.
/// </summary>
public enum TrayContextMenuHorizontalAlignment
{
    /// <summary>
    /// Aligns the left edge of the menu with the anchor point.
    /// </summary>
    Left,

    /// <summary>
    /// Aligns the horizontal center of the menu with the anchor point.
    /// </summary>
    Center,

    /// <summary>
    /// Aligns the right edge of the menu with the anchor point.
    /// </summary>
    Right,
}